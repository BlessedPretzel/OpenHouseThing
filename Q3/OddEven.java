package Q3;

/* WAP to check if a = 12 is odd or even number
 */
class OddEven {
    public static void main(String[] args) {
        int a = 12;
        if (a % 2 == 0) {
            System.out.println("a is even number");
        } 
        else {
            System.out.println("b is odd number");
        }
    }
}